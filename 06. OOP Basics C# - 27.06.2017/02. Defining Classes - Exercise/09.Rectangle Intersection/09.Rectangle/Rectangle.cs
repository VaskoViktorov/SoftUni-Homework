
public class Rectangle
{
    private string id;
    private double width;
    private double height;
    private double horizontalCord;
    private double verticalCord;

    public string ID { get { return this.id; } set { this.id = value; } }
    public double Width { get { return this.width; } set { this.width = value; } }
    public double Height { get { return this.height; } set { this.height = value; } }
    public double HorizontalCord { get { return this.horizontalCord; } set { this.horizontalCord = value; } }
    public double VerticalCord { get { return this.verticalCord; } set { this.verticalCord = value; } }
}

